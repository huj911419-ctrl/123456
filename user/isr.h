#ifndef _isr_h
#define _isr_h

#include "zf_common_headfile.h"

/* ========================================================================
 * 中断服务程序声明头文件
 * ========================================================================
 * 本文件声明所有中断服务程序（ISR）的函数原型。
 * 实际的ISR实现在 isr.c 中。
 *
 * TC264D 中断架构：
 *   - 每个中断源有独立的优先级（1-255，数值越大优先级越高）
 *   - 每个中断可分配给 CPU0、CPU1 或 DMA 控制器处理
 *   - 同一中断函数可由多个中断源共享（通过标志位区分）
 *
 * 本工程使用的中断资源：
 *   - CCU60_CH0 PIT：PID控制周期中断（8ms）
 *   - CCU60_CH1 PIT：IMU数据更新中断（5ms）
 *   - ERU通道：摄像头VSYNC/DMA同步中断
 *   - DMA通道5：摄像头数据DMA传输完成中断
 *   - UART0-3：调试串口、摄像头、无线模块、电机驱动通信中断
 * ======================================================================== */

#endif
