/**
 * ========================================================================
 * ImageTransfer.h - 图像数据传输模块头文件
 * ========================================================================
 * 通过UART0将二值化图像、边线坐标、调试参数发送到PC调试软件。
 * 用于离线分析视觉算法效果，调试参数配置。
 * ======================================================================== */
#ifndef IMAGE_TRANSFER_H
#define IMAGE_TRANSFER_H

/**
 * @brief 发送图像数据和调试信息到PC
 *
 * 通过UART0发送以下数据包（根据分频配置选择性发送）：
 *   包1 - 二值化图像（705字节压缩位图）
 *   包2 - 边线坐标（360字节，每行6字节：左边+右边+中线）
 *   包3 - TFT参数（41字节：误差、阈值、速度等调试值）
 *   包4 - 遥测数据（60字节：PID状态、RA状态、路线信息、电压等）
 *
 * 分频系数由 App_Config.h 中的 UART0_*_DIV 宏控制。
 * send_image_uart0_runtime() uses UART0_RUN_*_DIV during motor run.
 */
void send_image_uart0(void);
void send_image_uart0_runtime(void);

#endif
