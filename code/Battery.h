/**
 * ========================================================================
 * Battery.h - 电池电压监测模块头文件
 * ========================================================================
 * 提供电池电压采集、滤波和低电量检测接口。
 * 用于监控锂电池电压，防止过放损坏电池。
 * ======================================================================== */
#ifndef CODE_BATTERY_H_
#define CODE_BATTERY_H_

#include "zf_common_headfile.h"

/**
 * 初始化电池监测ADC通道和第一次采样
 * 在系统启动时调用一次
 */
void battery_init(void);

/**
 * 周期性电池电压检测（在主循环中调用）
 * 内部包含分频逻辑，不会每帧都执行ADC采样
 */
void battery_check(void);

/**
 * 获取电池电压（浮点数，单位：伏特）
 * @return 电池电压值，如12.0表示12V
 */
float battery_get_voltage(void);

/**
 * 获取电池电压x10（整数，单位：0.1伏特）
 * 避免浮点运算，用于TFT显示和整数比较
 * @return 电池电压x10，如120表示12.0V
 */
uint16 battery_get_voltage_x10(void);

/**
 * 获取电池ADC原始值（12位，0~4095）
 * 用于调试显示，确认ADC采样是否正常
 * @return ADC原始值
 */
uint16 battery_get_adc(void);

/**
 * 获取低电量标志
 * @return 0=电量正常，1=电量过低（<=11.5V）
 * 低电量时LED全部点亮作为警示
 */
uint8 battery_is_low(void);

#endif
