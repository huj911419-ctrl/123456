#include "Neg_pressure.h"
#include "zf_driver_pwm.h"

//-------------------------------------------------------------------------------------------------------------------
//  函数名称     负压PWM初始化
//  参数说明     frequency     PWM频率（Hz）
//  参数说明     duty_percent 占空比（0-10000，对应0%-100%）
//  返回值     无
//  使用示例   neg_pressure_init(10000, 5000);  // 10kHz，50%占空比
//-------------------------------------------------------------------------------------------------------------------
void neg_pressure_init(uint32 frequency, uint16 duty_percent)
{
    pwm_init(ATOM0_CH0_P21_2, frequency, duty_percent);
}

//-------------------------------------------------------------------------------------------------------------------
//  函数名称     设置负压PWM占空比
//  参数说明     duty_percent 占空比（0-10000，对应0%-100%）
//  返回值     无
//  使用示例   neg_pressure_set_duty(3000);  // 30%占空比
//-------------------------------------------------------------------------------------------------------------------
void neg_pressure_set_duty(uint16 duty_percent)
{
    pwm_set_duty(ATOM0_CH0_P21_2, duty_percent);
}