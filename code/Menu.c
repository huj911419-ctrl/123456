//  ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÓŋÚĢïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―Ņ­ïŋ―ïŋ―ïŋ―ïŋ― 10ms ïŋ―ïŋ―Ęąïŋ―ïŋ―ïŋ―ïŋ―
// ================================================================
void key_process(void)
{
    uint8 key = key_scan();
    if (key == 0)
        return;

    // ---- KEY1 / KEY2ïŋ―ïŋ―ïŋ―ïŋ―Ōģïŋ―ïŋ―ïŋ―Îšïŋ―ÄĢĘ―ïŋ―ïŋ―ïŋ―ïŋ―Ð§ïŋ―ïŋ―----
    if (key == 1) // ïŋ―ïŋ―ŌŧŌģ
    {
        now_page = (now_page + 1) % PAGE_MAX;
        menu_cursor = 0;
        tft180_clear();
        return;
    }
    if (key == 2) // ïŋ―ïŋ―ŌŧŌģ
    {
        now_page = (now_page == 0) ? (PAGE_MAX - 1) : (now_page - 1);
        menu_cursor = 0;
        tft180_clear();
        return;
    }

    // ---- KEY3 / KEY4ïŋ―ïŋ―ïŋ―Éēïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ÎŠ ----
    uint8 adjust_mode = dip_is_adjust_mode();
    MenuPageDef *page = &g_pages[now_page];
    uint8 item_cnt = page->item_count;

    if (!adjust_mode)
    {
        // ---- ŅĄïŋ―ïŋ―ÄĢĘ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―Æķïŋ―ïŋ―ïŋ―ïŋ― ----
        if (item_cnt == 0)
            return; // ïŋ―ïŋ―Ōģïŋ―ÞŋÉĩïŋ―ïŋ―îĢŽïŋ―ïŋ―ïŋ―ïŋ―

        if (key == 3) // ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
            menu_cursor = (menu_cursor == 0) ? (item_cnt - 1) : (menu_cursor - 1);

        if (key == 4) // ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―ïŋ―
            menu_cursor = (menu_cursor + 1) % item_cnt;
    }
    else
    {
        // ---- ïŋ―ïŋ―ïŋ―ïŋ―ÄĢĘ―ïŋ―ïŋ―ïŋ―ÞļÄĩïŋ―Į°ŅĄïŋ―ïŋ―ïŋ―ïŋ―ïŋ―Öĩ ----
        if (item_cnt == 0)
            return; // ïŋ―ïŋ―Ōģïŋ―ÞŋÉĩïŋ―ïŋ―îĢŽïŋ―ïŋ―ïŋ―ïŋ―

        cursor_clamp();
        MenuItem *cur = &page->items[menu_cursor];

        if (key == 3) // ïŋ―ïŋ―Öĩïŋ―ïŋ―ïŋ―ïŋ―
        {
            if (*cur->value + cur->step <= cur->max)
                *cur->value += cur->step;
            else
                *cur->value = cur->max;
        }
        if (key == 4) // ïŋ―ïŋ―Öĩïŋ―ïŋ―ïŋ―ïŋ―
        {
            if (*cur->value - cur->step >= cur->min)
                *cur->value -= cur->step;
            else
                *cur->value = cur->min;
        }
    }
}

