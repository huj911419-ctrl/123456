/**
 * ========================================================================
 * TFT_show_image.h - TFT调试显示模块头文件
 * ========================================================================
 * 在128x160 TFT屏幕上显示二值化图像和叠加的调试信息。
 *
 * 屏幕布局：
 *   y=0  ~ y=79  : 灰度/二值图像 + 叠加的边界线/中线/拐点/路口检测框
 *                   蓝色线=左边界  绿色线=右边界  红色线=中线
 *                   黄色X=左拐点   洋红色X=右拐点  青色框=路口检测框
 *   y=84 ~ y=115 : ERR/ROW/THR/RS/DT/WC 等调试数值
 *   x=112~159    : 电机速度、RA状态、yaw角、电池电压等关键值
 *
 * 必须在 track_fusion_update() 之后调用，以确保显示最新数据。
 * ======================================================================== */
#ifndef _TFT_SHOW_IMAGE_H_
#define _TFT_SHOW_IMAGE_H_

#include "zf_common_headfile.h"

/**
 * @brief TFT调试显示主函数
 *
 * 显示二值化图像底图 + 叠加边界线 + 调试数值。
 * 由主循环中的 menu_show() 调用（仅主页）。
 *
 * 显示内容：
 *   1. 二值化图像（黑白，白色=赛道，黑色=背景）
 *   2. 左边界线（蓝色）、右边界线（绿色）、中线（红色）
 *   3. 拐点标记（黄色X=左拐点，洋红色X=右拐点）
 *   4. 路口检测框（青色矩形）
 *   5. 右侧状态栏：电机速度、RA标志、yaw角、电池电压
 *   6. 底部调试栏：误差、有效行、阈值、速度原因等
 */
void draw_line(void);


#endif
